class Producto {
  final int id;
  final String name;
  final String description;
  final double price;
  final String image;
  final int discount;
  final String createdAt;

  Producto({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.discount,
    required this.createdAt,
  });
}
